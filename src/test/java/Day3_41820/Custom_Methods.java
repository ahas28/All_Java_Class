package Day3_41820;

public class Custom_Methods {

//purpose of those user defined custom methods so that i can reuse it again and again
//and call it on my main method when i need to use them

    //user defined custom methods will be created here

    //let's create one for void first
    public static void voidAdd(int a, int b) {
        int result = a + b;
        System.out.println("My addition results is" + result);
    }// end of void method

    //create same method as return method
    public static int returnAdd(int a, int b) {
        int results = a + b;
        System.out.println("My addition results is " + results);
        return results; //will return the result C
    }//end of void method

}//end of java class













